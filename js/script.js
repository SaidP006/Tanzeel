let currentLang = 'en';

// ============================================================
// SWITCH LANGUAGE FUNCTION
// ============================================================
function switchLanguage(lang) {
    currentLang = lang;
    const elements = document.querySelectorAll('[class*="lang-"]');
    
    elements.forEach(el => {
        const classes = el.className.split(' ');
        let langKey = null;
        
        for (let cls of classes) {
            if (cls.startsWith('lang-')) {
                langKey = cls.replace('lang-', '');
                break;
            }
        }
        
        if (langKey && langData[lang] && langData[lang][langKey] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = langData[lang][langKey];
            } else {
                el.textContent = langData[lang][langKey];
            }
        }
    });
    
    document.getElementById('langLabel').textContent = lang === 'en' ? 'Kiswahili' : 'English';
    document.getElementById('htmlTag').lang = lang === 'en' ? 'en' : 'sw';
    localStorage.setItem('tanzeel-lang', lang);
}

// ============================================================
// COUNTER ANIMATION
// ============================================================
function animateCounters() {
    const counters = document.querySelectorAll('.counter');
    counters.forEach(counter => {
        const target = parseInt(counter.getAttribute('data-target'));
        const duration = 2000;
        const step = Math.max(1, Math.floor(target / 60));
        let current = 0;
        
        const updateCounter = () => {
            current += step;
            if (current >= target) {
                counter.textContent = target;
                return;
            }
            counter.textContent = current;
            requestAnimationFrame(updateCounter);
        };
        
        updateCounter();
    });
}

// ============================================================
// CHECK AND HANDLE LOGO IMAGES
// ============================================================
function handleLogos() {
    // Check header logo
    const headerLogo = document.getElementById('headerLogo');
    if (headerLogo) {
        headerLogo.onerror = function() {
            this.style.display = 'none';
            const fallback = this.parentElement.querySelector('.brand-icon-fallback');
            if (fallback) {
                fallback.classList.remove('d-none');
            }
        };
    }
    
    // Check footer logo
    const footerLogo = document.getElementById('footerLogo');
    if (footerLogo) {
        footerLogo.onerror = function() {
            this.style.display = 'none';
            const fallback = this.parentElement.querySelector('.footer-logo-fallback');
            if (fallback) {
                fallback.classList.remove('d-none');
            }
        };
    }
}

// ============================================================
// INITIALIZE
// ============================================================
document.addEventListener('DOMContentLoaded', function() {
    // Load saved language
    const savedLang = localStorage.getItem('tanzeel-lang') || 'en';
    switchLanguage(savedLang);
    
    // Language toggle
    document.getElementById('langToggle').addEventListener('click', function() {
        const newLang = currentLang === 'en' ? 'sw' : 'en';
        switchLanguage(newLang);
    });
    
    // Initialize AOS
    AOS.init({
        duration: 800,
        once: true,
        offset: 100
    });
    
    // Handle logos (show fallback if logo not found)
    handleLogos();
    
    // Animate counters on scroll
    const statSection = document.querySelector('.stats-section');
    if (statSection) {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateCounters();
                    observer.disconnect();
                }
            });
        }, { threshold: 0.3 });
        observer.observe(statSection);
    }
    
    // Preloader
    window.addEventListener('load', function() {
        setTimeout(() => {
            document.getElementById('preloader').classList.add('hide');
        }, 1000);
    });
    
    // Navbar scroll effect
    const navbar = document.querySelector('.glass-navbar');
    window.addEventListener('scroll', function() {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });
    
    // Initialize Particles
    if (document.getElementById('particles-js')) {
        particlesJS('particles-js', {
            particles: {
                number: { value: 80, density: { enable: true, value_area: 800 } },
                color: { value: '#ffffff' },
                opacity: { value: 0.3, random: true },
                size: { value: 3, random: true },
                line_linked: { enable: true, distance: 150, color: '#ffffff', opacity: 0.1, width: 1 },
                move: { enable: true, speed: 2, direction: 'none', random: true, straight: false, out_mode: 'out' }
            },
            interactivity: {
                detect_on: 'canvas',
                events: { onhover: { enable: true, mode: 'repulse' }, onclick: { enable: true, mode: 'push' } }
            }
        });
    }
});